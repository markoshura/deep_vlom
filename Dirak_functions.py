    x2T = xT**2
    x3T = xT**3
    x4T = xT**4
    x5T = xT**5
    x6T = xT**6
    x7T = xT**7
    return cf0 + cf1 * xT + cf2 * (2. * x2T - 1.) + cf3 * (4.*x3T - 3. * xT) + cf4 * (8. * x4T - 8. * x2T + 1.) + \
           cf5 * (16. * x5T - 20. * x3T + 5. * xT) + cf6 * (32. * x6T - 48. * x4T + 18. * x2T - 1.) + \
           cf7 * (64. * x7T - 112. * x5T + 56. * x3T - 7. * xT)


